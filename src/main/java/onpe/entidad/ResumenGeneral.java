package onpe.entidad;

public class ResumenGeneral {
	
	private String p1;
	private String p2;
	private String validoP1;
	private String validoP2;
	private String totalActas;
	private String totalProcesados;
	private String contabilizadas;
	private String electoresHabiles;
	private String TotalVotantes;
	private String participacion;
	private String ausentismo;

	public ResumenGeneral() {

	}

	public String getP1() {
		return p1;
	}

	public void setP1(String p1) {
		this.p1 = p1;
	}

	public String getP2() {
		return p2;
	}

	public void setP2(String p2) {
		this.p2 = p2;
	}

	public String getValidoP1() {
		return validoP1;
	}

	public void setValidoP1(String validoP1) {
		this.validoP1 = validoP1;
	}

	public String getValidoP2() {
		return validoP2;
	}

	public void setValidoP2(String validoP2) {
		this.validoP2 = validoP2;
	}

	public String getTotalActas() {
		return totalActas;
	}

	public void setTotalActas(String totalActas) {
		this.totalActas = totalActas;
	}

	public String getTotalProcesados() {
		return totalProcesados;
	}

	public void setTotalProcesados(String totalProcesados) {
		this.totalProcesados = totalProcesados;
	}

	public String getContabilizadas() {
		return contabilizadas;
	}

	public void setContabilizadas(String contabilizadas) {
		this.contabilizadas = contabilizadas;
	}

	public String getElectoresHabiles() {
		return electoresHabiles;
	}

	public void setElectoresHabiles(String electoresHabiles) {
		this.electoresHabiles = electoresHabiles;
	}

	public String getTotalVotantes() {
		return TotalVotantes;
	}

	public void setTotalVotantes(String totalVotantes) {
		TotalVotantes = totalVotantes;
	}

	public String getParticipacion() {
		return participacion;
	}

	public void setParticipacion(String participacion) {
		this.participacion = participacion;
	}

	public String getAusentismo() {
		return ausentismo;
	}

	public void setAusentismo(String ausentismo) {
		this.ausentismo = ausentismo;
	}
}
